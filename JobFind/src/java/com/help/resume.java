
package com.help;

public class resume {
    private int did;
    private String resume;
    public resume()
    {
        
    }
    public resume(int did, String resume) {
        this.did = did;
        this.resume = resume;
    }

    public int getDid() {
        return did;
    }

    public void setDid(int did) {
        this.did = did;
    }

    public String getResume() {
        return resume;
    }

    public void setResume(String resume) {
        this.resume = resume;
    }
    
    
}
